<?php

declare(strict_types=1);

use App\Models\User;

test('to array', function (): void {
    $user = User::factory()->create()->refresh();

    expect(array_keys($user->toArray()))
        ->toBe([
            'id',
            'name',
            'email',
            'email_verified_at',
            'is_online',
            'last_seen_at',
            'created_at',
            'updated_at',
            'webhook_url',
            'webhook_secret',
            'workflow_api_token',
            'is_active',
        ]);
});
